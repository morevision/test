money, c500, c100, c50, c10 = 0, 0, 0, 0, 0   # 여러 개의 변수를 초기화 (각각의 변수에 0을 할당)
# money: 사용자가 입력할 금액을 저장할 변수
# c500, c100, c50, c10: 각각 500원, 100원, 50원, 10원짜리 동전 개수를 저장할 변수
money=int(input("정수를 입력하시오"))  # 사용자에게 금액을 입력받아 money 변수에 저장
# input() 함수 : 사용자에게 값을 입력받음
# int() 함수 : 문자열에 들어온 입력값을 정수로 변환
c500 = money // 500    # 500원짜리 동전이 몇 개 필요한지 계산
# // 연산자는 정수 나눗셈, 나머지는 버리고 몫만 계산
money %= 500    # 500원짜리 동전으로 거슬러 준 금액을 뺀 남은 금액을 갱신
# %=연산자는 나머지를 계산 후 그 값을 다시 money에 저장
c100 = money // 100    # 남은 금액에서 100원짜리 동전이 몇 개 필요한지 계산
money %= 100    # 이때 money는 100원짜리로 거슬러 준 후 남은 금액
c50 = money // 50  # 남은 금액에서 50원짜리 동전이 몇 개 필요한지 계산
money %= 50  # 50원짜리 동전으로 나누고 남은 금액 계산 
c10 = money // 10  # 남은 금액에서 10원짜리 동전이 몇 개 필요한지 계산
money %= 10  # 10원으로 나누고 남은 금액 계산 (10원 미만 금액은 동전으로 교환할 수 없음)
print("\n 500원짜리 ==> %d개" % c500)  # c500의 값을 %d 자리에 넣어 출력 (500원짜리 동전 개수)
# print()는 화면에 내용을 출력하는 함수
# \n은 줄바꿈 문자
# %d는 정수 자리에 변수 값을 넣기 위한 자리 표시자
print(" 100원짜리 ==> %d개 " % c100)  # c100의 값을 %d 자리에 넣어 출력 (100원짜리 동전 개수)
print(" 50원짜리 ==> %d개 " % c50)  # c50의 값을 %d 자리에 넣어 출력 (50원짜리 동전 개수)
print(" 10원짜리 ==> %d개 " % c10)  # c10의 값을 %d 자리에 넣어 출력 (10원짜리 동전 개수)
print(" 바꾸지 못한 잔돈 ==> %d원" % money)  # 동전으로 교환하지 못한 잔돈 출력
# 정리: 사용자가 입력한 금액을 500원, 100원, 50원, 10원짜리 동전으로 바꾸는 계산을 수행, 각 동전이 몇 개 필요한지 출력하는 코드
